public class Encapsulate {
    private String name;
    private int rollNo;

    public void setName(String name){
        this.name = name;
    }

    public String getName(){
        return name;
    }


    public void setRollNo(int rollNo){
        this.rollNo = rollNo;
    }

    public int getRollNo(){
        return rollNo;
    }
}


